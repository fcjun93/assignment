package agile;

/**
 *
 * @author Benjamin
 */
public class Affiliates {
    private String name,location, type;
    private int month,year;

    public Affiliates() {
    }

    public Affiliates(String name, String location, String type, int month, int year) {
        this.name = name;
        this.location = location;
        this.type = type;
        this.month = month;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

}

